<script>
    document.querySelectorAll('.nav-faqs a').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            e.preventDefault();

            const targetId = this.getAttribute('href').substring(1); // Lấy ID của phần tử được liên kết
            const targetElement = document.getElementById(targetId); // Tìm phần tử theo ID

            if (targetElement) {
                const offsetTop = targetElement.offsetTop - 80; // Điều chỉnh vị trí scroll bù đắp phần header
                window.scrollTo({
                    top: offsetTop,
                    behavior: 'smooth' // Scroll mượt
                });
            }
        });
    });

    document.addEventListener("DOMContentLoaded", function() {
        var answerContainers = document.querySelectorAll('.answer-container');

        answerContainers.forEach(function(answerContainer) {
            answerContainer.addEventListener('click', function() {
                var answer = answerContainer.querySelector('.answer');

                // Kiểm tra trạng thái hiện tại của answer
                if (answer.style.display === 'block') {
                    // Nếu đang hiển thị, ẩn đi
                    $(answer).slideUp();
                } else {
                    // Nếu đang ẩn, hiển thị lên
                    $(answer).slideDown();
                }
            });
        });
    });
</script>
